export type TranslationKey = keyof typeof translations.en;

export function t(lang: Lang, key: TranslationKey, ...args: string[]): string {
  let text = translations[lang][key] ?? translations.en[key] ?? key;
  args.forEach((arg, i) => {
    text = text.replace('%', arg);
  });
  return text;
}
